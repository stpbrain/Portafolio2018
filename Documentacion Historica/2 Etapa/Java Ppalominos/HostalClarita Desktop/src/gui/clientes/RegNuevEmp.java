    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
         //falta validar el doble registro !!!!
            RegistroNuevaEmpBLL nemp = new RegistroNuevaEmpBLL();
            String n=tfNombre.getText();
            String r=tfRut.getText();
            String p=tfPass.getText();
            String d= tfDireccion.getText();
            String t= tfTelefono.getText();
            String m=tfMail.getText();
        try {
            
            if(nemp.crearEmpresa(n,r, p,d,t,m))
              {
                  JOptionPane.showMessageDialog(null,"Empresa Registrada OK");
                  tfNombre.setText("");
                  tfRut.setText("");
                  tfPass.setText("");
                  tfDireccion.setText("");
                  tfTelefono.setText("");
                  tfMail.setText("");
              }
            
        } catch (SQLException ex) {
            Logger.getLogger(RegNuevEmp.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("problema en la insercion de empresa en la base !!");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

